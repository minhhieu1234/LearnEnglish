                    <h1>Countable nouns</h1>
                    <div class="countable-nouns">
                        <p>Some nouns refer to things which, in English, are treated as separate items which can be counted. These are called countable nouns. Here are some examples:</p>
                    <p>a <strong>car</strong>, three <strong>cars</strong>

                        <br>my <strong>cousin</strong>, my two <strong>cousins</strong>
                        
                        <br>a <strong>book</strong>, a box full of <strong>books</strong>
                        
                        <br>a <strong>city</strong>, several big <strong>cities</strong></p>
                    </div>
                    <h3>Singular and plural</h3>
                    <div class="singular-and-plural">
                        
                        <p>Countable nouns can be singular or plural. They can be used with a/an and with numbers and many other determiners (e.g. these, a few):</p>
                        <p>She’s got <strong>two sisters</strong> and <strong>a young brother</strong>.

                            <br>Most people buy <strong>things</strong> like <strong>cameras</strong> and <strong>MP3-players</strong> online <strong>these days</strong>.
                            
                            <br><strong>These shoes</strong> look old now.
                            
                            <br>I’ll take <strong>a few magazines</strong> with me for <strong>the flight</strong></p>
                    </div>
                    <h4>See also</h4>
                    <div class="see-also">
                      <p>Determiners</p>
                      <p>Singular and plural nouns</p>
                      <p>Nouns</p>
                    </div>
                    <h1>Uncountable nouns</h1>
                    <div class="uncountable-nouns">
                      <p>In English grammar, some things are seen as a whole or mass. These are called uncountable nouns, because they cannot be separated or counted.</p>
                    </div>
                    <br>
                    <div class="some-examples">
                      <p>Some examples of uncountable nouns are:

                        <br><br>Ideas and experiences: <span>advice, information, progress, news, luck, fun, work</span>
                        
                        <br><br>Materials and substances: <span>water, rice, cement, gold, milk</span>
                        
                        <br><br>Weather words: <span>weather, thunder, lightning, rain, snow</span>
                        
                        <br><br>Names for groups or collections of things: <span>furniture, equipment, rubbish, luggage</span>
                        
                        <br><br>Other common uncountable nouns include: <span>accommodation, baggage, homework, knowledge, money, permission, research, traffic, travel.</span></p>
                    </div>
                    <div class="these-nouns">
                      <p>These nouns are not used with a/an or numbers and are not used in the plural.</p>
                    </div>
                    <div class="these-nouns-examples">
                      <p>We’re going to get new <strong>furniture</strong> for the living room.

                        <br><br>Not: <span>We’re going to get a new furniture for the living room</span>. or <span>We’re going to get new furnitures for the living room.</span>
                        
                        <br><br>We had terrible <strong>weather</strong> last week.
                        
                        <br><br>Not: <span>We had a terrible weather last week.</span>
                        
                        <br><br>We need <strong>nice</strong> next time we go shopping.</p>
                    </div>
                    <div class="some-nouns-always">
                      <p>Some nouns always have plural form but they are uncountable because we cannot use numbers with them.</p>
                    </div>
                    <div class="some-nouns-always-example">
                      <p>I bought <strong>two pairs of trousers.</strong>

                        <br><br>Not: <span>two pairs of trousers.</span></p>
                    </div>
                    <div class="other-nouns">
                      <p>Other nouns of this type are: shorts, pants, pyjamas, glasses (for the eyes), binoculars, scissors</p>
                    </div>
                    <div class="block-block-content-warning">
                      <h4>Warning</h4>
                      <div class="some-nouns-block-warning">
                        <p>Some nouns which are uncountable in English are countable in other languages (e.g. accommodation, advice, furniture, information):</p>
                      </div>
                      <div class="some-nouns-block-warning-examples">
                        <p>They can give you some information about accommodation at the tourist office.

                          <br><br>Not: <span>They can give you some informations about accommodations at the tourist office</span>.
                          
                          <br><br>Can you give me some <strong>advice</strong> about buying a second-hand car?
                          
                          <br><br>Not: <span>They can give you some informations about accommodations at the tourist office?</span></p>
                      </div>
                    </div>
                    <div class="good-learning">
                      <p>A good learner’s dictionary will tell you whether a noun is countable or uncountable.</p>
                    </div>
                    <div class="quality-expressions">
                      <h3>Quantity expressions (a bit/piece)</h3>
                      <div class="to-refer-to-one">
                        <p>To refer to one or more quantities of an uncountable noun, expressions such as a bit of, a piece of, an item of or words for containers and measures must be used:</p>
            
                      </div>
                      <div class="to-refer-to-one-example">
                        <p>He bought a very expensive <strong>piece of furniture</strong> for his new apartment.

                          <br><br>Maggie always has some exciting <strong>bits of news</strong> when she comes to see us.
                          
                          <br><br>I think we’ll need <strong>five bags of cement</strong> for the patio.
                          
                          <br><br>There’s <strong>litre of milk</strong> in the fridge for you. And I bought <strong>you a bar of chocolate.</strong></p>
                      </div>
                    </div>
                    <div class="determiners">
                      <h3>Determiners (my, some, the)</h3>
                      <div class="uncountable-nouns-determiners">
                        <p>Uncountable nouns can be used with certain determiners (e.g. my, her, some, any, no, the, this, that) and expressions of quantity (e.g. a lot of, (a) little):</p>
                        <div class="derterminers-my-some-the">
                          <p>They gave me some information about courses and scholarships and things.

                            <br><br>Have you heard the news? Fran’s getting engaged.
                              
                              <br><br>She’s been studying hard and has made a lot of progress.
                              
                              <br><br>There’s no work to do here, so you can go home if you like.
                              
                              <br><br>This milk’s a bit old, I’m afraid.</p>
                        </div>
                        <h4>See also</h4>
                        <p>Determiners(the,my,some,this)</p>
