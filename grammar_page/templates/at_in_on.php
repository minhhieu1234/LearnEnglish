              <h3>Usage Prepositons of time: at, in, on in this video</h3>
              <h1>1. How to use "at", "in", "on":</h1>
              <p>"In – On -At" is used in turn in expressing the time period: general – quite broad – specific. Thus, each type of preposition will be used in different ways. Concrete:</p>
              <div class="block-block-image-content">
                <img src="https://elight.edu.vn/wp-content/uploads/2019/01/Ca%CC%81ch-dung-in-on-at.jpg" alt="Usage 'at', 'in','on'">
              </div>
              
              <div class="block-block-content-list-container">
                <h2>2.1 The preposition "In" indicates location</h2>
                <p>"In" will be used for general locations such as region, country, city ... is broadly inclusive, for example:</p>
                <ul>
                   <li>In Ha Noi</li>
                   <li>In London</li>
                   <li>In VietNam  </li>
                   <li>In china</li>
                   <li>In Asian</li>
                </ul>
                <h2>2.2. The preposition "In" indicates time</h2>
                <p>"In" is a phrase used with time phrases to refer to a general millennial span, decade, year, month, week, day,... For example: </p>
                <ul>
                  <li>In Winter, in Sring</li>
                  <li>In the 21th century</li>
                  <li>In the 90's</li>
                  <li>In 2020</li>
                  <li>In July</li>
                </ul>
                <h2>2.3. The preposition "On" indicates location</h2>
                <p>The preposition "on" will use more specific places, such as:</p>
                <ul>
                  <li>On Doan Ke Thien Street, on Tran Hung Dao Street </li>
                  <li>On a bus, On a train, On a plane, On a ship</li>
                </ul>
                <h2>2.4. The preposition "On" indicates time</h2>
                <p>The preposition "on" indicates more specific time periods than "in"; such as weekdays, specific dates and holidays that contain the word "day". Example:</p>
                <ul>
                  <li>On Monday, On Sunday</li>
                  <li>On July, 15th</li>
                  <li>On Valentine's day, On Labor day</li>
                  
                </ul>
                <h2>2.5. The preposition "At" indicates location</h2>
                <p>The preposition "at" will indicate precise locations, which have specific addresses. Example:</p>
                <ul>
                  <li>At 169 Doan Ket Thien, At 98 Tran Hung Dao</li>
                  <li>At home, at work, at university</li>
                </ul>
                <h2>2.6. The preposition "At" indicates time</h2>
                <p>This is a commonly used preposition to refer to a specific timeline. So it usually refers to: exact time, a special occasion that does not contain the word "day"; or used with words containing "time" and "moment". Example: </p>
                <ul>
                  <li>At 6 a.m, at 8 o’clock </li>
                  <li>At dawn (vào lúc bình minh); At sunrise (lúc mặt trời mọc); At midnight (vào lúc trời tối), At lunch (vào buổi trưa).</li>
                  <li>At Christmas, at Mid – Autumn Festival </li>
                  <li>At the moment</li>
                </ul>
